@app.route("/submit", methods=["GET", "POST"])
@limiter.limit("30 per hour", key_func=creator_key)
def submit():
    if request.method == "GET":
        return """
        <h2>Submit content</h2>
        <form method="post" action="/submit">
          <label>Content ID</label><br>
          <input name="content_id" required><br><br>
          <label>Creator ID</label><br>
          <input name="creator_id"><br><br>
          <label>Text</label><br>
          <textarea name="text" rows="6" cols="60" required></textarea><br><br>
          <button type="submit">Submit</button>
        </form>
        """

    data = get_request_data()
    content_id = data.get("content_id")
    text = data.get("text", "")
    creator_id = data.get("creator_id")
    if not content_id or not text:
        return jsonify({"error": "content_id and text required"}), 400

    stylo = compute_stylometry_score(text)
    groq = call_groq_stub(text)
    final_score, category = compute_combined_score(groq, stylo)

    if category == "ai":
        label = LABEL_HIGH_AI
    elif category == "human":
        label = LABEL_HIGH_HUMAN
    else:
        label = LABEL_UNCERTAIN

    signals = {"groq": groq, "stylometry": stylo}
    notes = "Computed by backend"
    audit_id = persist_audit(content_id, creator_id, category, final_score, signals, label, notes)

    return jsonify({
        "content_id": content_id,
        "result": category,
        "confidence": round(final_score, 4),
        "label": label,
        "audit_id": audit_id,
        "signals": {"groq": round(groq, 4), "stylometry": round(stylo, 4)},
    }), 200

@app.route("/log", methods=["GET"])
def get_log():
    content_id = request.args.get("content_id")
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    c = conn.cursor()
    if content_id:
        c.execute("SELECT * FROM audit WHERE content_id = ? ORDER BY id DESC LIMIT 50", (content_id,))
    else:
        c.execute("SELECT * FROM audit ORDER BY id DESC LIMIT 50")
    rows = [dict(r) for r in c.fetchall()]
    conn.close()
    # parse signals_json
    for r in rows:
        try:
            r["signals"] = json.loads(r.get("signals_json") or "{}")
        except Exception:
            r["signals"] = {}
        r.pop("signals_json", None)
    return jsonify(rows)

@app.route("/appeal", methods=["GET", "POST"])
def appeal():
    if request.method == "GET":
        return """
        <h2>File an appeal</h2>
        <form method="post" action="/appeal">
          <label>Content ID</label><br>
          <input name="content_id" required><br><br>
          <label>Creator ID</label><br>
          <input name="creator_id" required><br><br>
          <label>Reason</label><br>
          <textarea name="reason" rows="6" cols="60" required></textarea><br><br>
          <button type="submit">Submit appeal</button>
        </form>
        """

    data = get_request_data()
    content_id = data.get("content_id")
    creator_id = data.get("creator_id")
    reason = data.get("reason", "")
    if not content_id or not creator_id or not reason:
        return jsonify({"error": "content_id, creator_id, and reason required"}), 400

    # link to most recent audit for content if available
    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()
    c.execute("SELECT id FROM audit WHERE content_id = ? ORDER BY id DESC LIMIT 1", (content_id,))
    row = c.fetchone()
    linked_audit_id = row[0] if row else None
    c.execute(
        "INSERT INTO appeals (timestamp, content_id, creator_id, reason, status, linked_audit_id) VALUES (?,?,?,?,?,?)",
        (datetime.utcnow().isoformat() + "Z", content_id, creator_id, reason, "under review", linked_audit_id),
    )
    conn.commit()
    appeal_id = c.lastrowid
    # annotate audit notes
    if linked_audit_id:
        c.execute("UPDATE audit SET notes = notes || ? WHERE id = ?", (" | appealed", linked_audit_id))
        conn.commit()
    conn.close()

    return jsonify({"content_id": content_id, "status": "under review", "appeal_id": appeal_id}), 200

if __name__ == "__main__":
    import sys
    port = int(sys.argv[1]) if len(sys.argv) > 1 else int(os.environ.get("PORT", "5000"))
    app.run(host="0.0.0.0", port=port, debug=True)
