import os
import sqlite3
import json
from datetime import datetime
from flask import Flask, request, jsonify
from dotenv import load_dotenv
from stylometry import compute_stylometry_score
from flask_limiter import Limiter
from flask_limiter.util import get_remote_address

load_dotenv()

GROQ_API_KEY = os.getenv("GROQ_API_KEY")

DB_PATH = os.path.join(os.path.dirname(__file__), "audit.db")

app = Flask(__name__)

# Rate limiter: default per-IP plus per-creator limits on submit
limiter = Limiter(key_func=get_remote_address, default_limits=["60 per hour"])
limiter.init_app(app)

def get_request_data():
    if request.is_json:
        return request.get_json(silent=True) or {}
    if request.form:
        return request.form.to_dict()
    return {}


def creator_key():
    # Use creator_id as rate limit key when available, otherwise fallback to IP
    try:
        data = get_request_data()
        creator = data.get("creator_id")
        if creator:
            return f"creator:{creator}"
    except Exception:
        pass
    return get_remote_address()


def init_db():
    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()
    c.execute(
        """
        CREATE TABLE IF NOT EXISTS audit (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            timestamp TEXT,
            content_id TEXT,
            creator_id TEXT,
            result TEXT,
            confidence REAL,
            signals_json TEXT,
            label_text TEXT,
            notes TEXT
        )
        """
    )
    c.execute(
        """
        CREATE TABLE IF NOT EXISTS appeals (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            timestamp TEXT,
            content_id TEXT,
            creator_id TEXT,
            reason TEXT,
            status TEXT,
            linked_audit_id INTEGER
        )
        """
    )
    conn.commit()
    conn.close()

init_db()

def call_groq_stub(text):
    """Return score in [0,1] where 1 = AI-generated using formal/casual marker heuristic."""
    if not GROQ_API_KEY:
        formal_markers = [
            "paradigm", "essential", "stakeholders", "collaborate", "important to note",
            "furthermore", "transformative", "implications", "mandate", "unintended consequences",
            "extensive", "literature", "tension", "valuations"
        ]
        casual_markers = ["ok so", "honestly", "lol", "imo", "ngl", "anyway", "kinda", "sorta"]
        
        text_lower = text.lower()
        formal_score = sum(1 for m in formal_markers if m in text_lower) / max(1, len(text_lower.split()) / 50)
        casual_score = sum(1 for m in casual_markers if m in text_lower)
        
        ai_likelihood = min(1.0, (formal_score * 0.15) - (casual_score * 0.1))
        return max(0.0, min(1.0, ai_likelihood))
    
    return 0.45 if len(text) % 2 == 0 else 0.55

WEIGHTS = {"groq": 0.6, "stylometry": 0.4}

LABEL_HIGH_AI = "High-confidence AI-generated: This content is very likely generated by AI (confidence: 90%+). If you are the creator and believe this is incorrect, you may file an appeal."
LABEL_HIGH_HUMAN = "High-confidence human-written: This content appears to be written by a human (confidence: 90%+). If you are the creator and believe this is incorrect, you may file an appeal."
LABEL_UNCERTAIN = "Uncertain attribution: The system is uncertain whether this content was written by a human or generated by AI (confidence: 50%–90%). Creator appeals are available."

def compute_combined_score(groq_score, stylo_score):
    final = WEIGHTS["groq"] * groq_score + WEIGHTS["stylometry"] * stylo_score
    # conflict rule: if signals differ a lot and final in warning range, downgrade to uncertain
    if abs(groq_score - stylo_score) >= 0.25 and 0.6 <= final < 0.9:
        # map to uncertain by returning a mid value
        return final, "uncertain"
    if final >= 0.9:
        return final, "ai"
    if final <= 0.1:
        return final, "human"
    return final, "uncertain"

def persist_audit(content_id, creator_id, result, confidence, signals, label_text, notes=""):
    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()
    c.execute(
        "INSERT INTO audit (timestamp, content_id, creator_id, result, confidence, signals_json, label_text, notes) VALUES (?,?,?,?,?,?,?,?)",
        (datetime.utcnow().isoformat() + "Z", content_id, creator_id, result, confidence, json.dumps(signals), label_text, notes),
    )
    conn.commit()
    rowid = c.lastrowid
    conn.close()
    return rowid

